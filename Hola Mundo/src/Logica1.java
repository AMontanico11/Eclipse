
public class Logica1 {

    public static void main(String[] args) {
    	
    	int contadorDeNumeros = 0;
    	
    	for (int i = 1;i<=16;i++) {
    		System.out.print(i+"  ");
    		contadorDeNumeros++;
    		
    		if(contadorDeNumeros==4) {
    			 System.out.println("");
    		}else if(contadorDeNumeros==8) {
    			System.out.println("");
    		}else if(contadorDeNumeros==12) {
    			System.out.println("");
    		}
    	}
    }
	
}
